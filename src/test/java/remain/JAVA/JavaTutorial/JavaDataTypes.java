package remain.JAVA.JavaTutorial;

public class JavaDataTypes {

    public static void main(String[] args){

        int myNum = 5;
        float myFloatNum = 5.22f;
        char myLetter = 'D';
        boolean myBool = true;
        String myText = "Hello";

        byte myByteNum = 100;
        System.out.println(myByteNum);

        short myShortNum = 5000;
        System.out.println(myShortNum);

        int myIntNum = 10000;
        System.out.println(myIntNum);

        long myLongNum = 1500000000L;
        System.out.println(myLongNum);

        float myFloatN = 5.44F;
        System.out.println(myFloatN);

        double myDoubleNum = 19.99d;
        System.out.println(myDoubleNum);

        boolean isJavaFun = true;
        boolean isFishTasty = false;
        System.out.println(isJavaFun);
        System.out.println(isFishTasty);

        char myGrade = 'B';
        System.out.println(myGrade);

        char myVar = 65, myVar2 = 66, myVar3 = 67;
        System.out.println(myVar + myVar2 + myVar3);
        System.out.println(myVar);
        System.out.println(myVar2);
        System.out.println(myVar3);

        int items = 50;
        float costPerItem = 9.99f;
        float totalCost = items * costPerItem;
        char currency = '$';

        System.out.println("Number of items: " + items);
        System.out.println("Cost per item: " + costPerItem + currency);
        System.out.println("Total cost = " + totalCost + currency);




    }
}
